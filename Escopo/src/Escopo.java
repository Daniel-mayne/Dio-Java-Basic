public class Escopo {
    public static void main(String[] args) throws Exception {
        System.out.println("Hello, World!");

        // No escopo, precisamos entender até onde cada variável é acessível. Temos as
        // variáveis da classe,
        // que são acessíveis em todos os métodos; também existem as variáveis de
        // método, que são acessíveis
        // apenas no próprio método, e as variáveis de bloco, que são acessíveis somente
        // no bloco onde foram definidas.

    }
}
